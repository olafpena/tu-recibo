package org.mexico.tgm.model;

public class Ruta {
	
	private String annio;
	private String quincena;
	private String nombreRecibo;
	
	public String getAnnio() {
		return annio;
	}
	public void setAnnio(String annio) {
		this.annio = annio;
	}
	public String getQuincena() {
		return quincena;
	}
	public void setQuincena(String quincena) {
		this.quincena = quincena;
	}
	public String getNombreRecibo() {
		return nombreRecibo;
	}
	public void setNombreRecibo(String nombreRecibo) {
		this.nombreRecibo = nombreRecibo;
	}
	

}
